package AdvanceJava;
public class Max_Value_of_Data_type {
    public static void main(String[] args) {
        System.out.println("The max value of Long:-   "+Long.MAX_VALUE);
        System.out.println("The max value of Integer:-    "+Integer.MAX_VALUE);
        System.out.println("\nThe value of current time in ms:-    " + System.currentTimeMillis());
        System.out.println("\nThe max value of Double:-   "+Double.MAX_VALUE);
        System.out.println("The max value of Char:-    "+Character.MAX_VALUE);
        System.out.println("The max value of Float:-   "+Float.MAX_VALUE);
    }
}
